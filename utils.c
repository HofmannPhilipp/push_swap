#include "push_swap.h"

void	print_array(int a[], int size)
{
	int	i;

	i = 0;
	while (i < size)
	{
		ft_putnbr_fd(a[i], 1);
		ft_putchar_fd('\n', 1);
		i ++;
	}
}
